    private void mostrarDatosCC(){
        DefaultTableModel tconsultaC = new DefaultTableModel();
        tconsultaC.addColumn("numFactura");
        tconsultaC.addColumn("codCliente");
        tconsultaC.addColumn("codProducto");
        tconsultaC.addColumn("cantidad");
        tconsultaC.addColumn("fechaFacturacionCompra");
        tconsultaC.addColumn("totalFactura");        
        tconsultaC.addColumn("codFormaPago");

        TablaCC.setModel(tconsultaC);
        if("".equals(getTfNumeroFactura().getText())){
            boolean busqueda = false;
            tconsultaC = con.mostrarDatos(tconsultaC, "compra", getTfNumeroFactura().getText(), "numFactura", busqueda, 2);
            TablaCC.setModel(tconsultaC);
        }
        else{
            boolean busqueda = true;
            tconsultaC = con.mostrarDatos(tconsultaC, "compra", getTfNumeroFactura().getText(), "numFactura", busqueda, 2);
            TablaCC.setModel(tconsultaC);
        }
    }
