    private void BRegistroFacturaCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BRegistroFacturaCompraActionPerformed
       guardarEstado();
        Compra compra = new Compra();
       Proveedor pro = new Proveedor();
       compra.setFechaFacturacionCompra(TfFechaFacturacionCompra.getText());
       pro.setNumeroDocumento(Integer.parseInt(TfDocumentoProveedor.getText()));
       int cantidadValue = (Integer) SCantidad.getValue();
       int formA = CBFormaPago.getSelectedIndex() + 1;
       int idProducto = Integer.parseInt(TfProductos.getText());
       String fecha = compra.getFechaFacturacionCompra();
       con.generarFacturaCompra(formA, pro.getNumeroDocumento(), fecha, cantidadValue, idProducto, TfValorTotal, TfNumFactura);   
       limpiar();
    }//GEN-LAST:event_BRegistroFacturaCompraActionPerformed

