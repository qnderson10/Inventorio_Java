        public void guardarEstado() {
        memento = new Modelo.FacturacionCompraMemento(
            TfFechaFacturacionCompra.getText(),
            TfValorTotal.getText(),
            TfDocumentoProveedor.getText(),
            TfProductos.getText()
        );
    }
    public void restaurarEstado() {
        if (memento != null) {
            TfFechaFacturacionCompra.setText(memento.getFechaFacturacionCompra());
            TfValorTotal.setText(memento.getValorTotal());
            TfDocumentoProveedor.setText(memento.getDocumentoProveedor());
            TfProductos.setText(memento.getProductos());
        }
    }
    
