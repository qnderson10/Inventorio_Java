    private void BRegistroFacturaVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BRegistroFacturaVentaActionPerformed
       Venta venta = new Venta();
       venta.setFechaFact(TfFechaFacturaVenta.getText());
       int formA = CBFormaPago.getSelectedIndex() + 1;
       String nombreEmpleado = TfNombreEmpleado.getText();
       int idProducto = Integer.parseInt(TfProductos.getText());
       String fecha = venta.getFechaFact();
       int cantidadValue = (Integer) SCantidad.getValue();
       int idCliente = Integer.parseInt(TfCodigoCliente.getText());
       con.generarFacturaVenta(idCliente, nombreEmpleado, formA, fecha, cantidadValue, idProducto, TfValorTotal, TFNumFactura);
    }//GEN-LAST:event_BRegistroFacturaVentaActionPerformed

